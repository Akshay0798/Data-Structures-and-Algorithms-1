public class PrettyPrinting {
    public static void main(String[] args) {
        float a=456.1293f;
        System.out.printf("Formatted Number is %.2f",a); //% - it's a placeHolder
        System.out.println();
        System.out.printf("Pie: %.2f",Math.PI);
        System.out.println();
        System.out.printf("My Name is %s and My GF name is %s","Akshay","Suppu");

    }
}
